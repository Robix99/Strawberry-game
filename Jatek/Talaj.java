	public int palyameret() {
		int valtozo=1;
		int n=0;
		while(valtozo>0) {
			System.out.println("Kerlek add meg a palya meretet!");
			Scanner sc = new Scanner(System.in);
			n = sc.nextInt();
			if(n<12) {
				System.out.println("A szam nem lehet 12-nel kisebb, kerlek adj meg egy nagyobb szamot!");
			}else if(n>25) {
				System.out.println("Ekkora palya nem idealis a jatekhoz. Kerlek adj meg 26-nal kisebb szamot!");
			}else {
				valtozo=0;
			}
			
		}
		return n;		
	}
	
	public void palyagen(String tomb[][]) {
		Random rand=new Random();
		for (int i=0; i<tomb.length; i++) {
			for(int j=0; j<tomb[i].length; j++) {
				int c=rand.nextInt(4)+1;
				if(j==0 || i==0 || i==tomb.length-1 || j==tomb.length-1) {
					tomb[i][j]=setMezo("▒");		//a palya szele
				}
				else if(j==5 || j==9) {
					tomb[i][j]=setMezo(" ");
				}else if(i==3 || i==7) {
					tomb[i][j]=setMezo(" ");
				}
				else if(c<=4&& c>=2) {
					tomb[i][j]=setMezo("█"	+ ""); 
				}else {
					tomb[i][j]=setMezo(" ");
				}
			}
		}
	}
	
